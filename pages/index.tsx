import Head from "next/head";
import styles from "../styles/Home.module.scss";
import Header from "../components/Header";
import CustomTable from "../components/CustomTable";
import { TableHeader } from "../lib/interface";
import { useRouter } from "next/router";
import { useSelector } from "react-redux";
import { selectAuthState } from "../store/authSlice";
import { SearchBox } from "../components/control/SearchBoxComponent";
import { useEffect, useState } from "react";
import { Button, Card, Container, Grid } from "@mui/material";
import { useGetKeyWords } from "../services/keywords";
import PaginationComponent from "../components/PaginationComponent";
import SearchFilterForm from "../components/form/SearchFilterForm";
import { Box } from "@mui/system";
import DialogComponent from "../components/DialogComponent";

interface KeywordDto {
  id: string;
  keyword: string;
  totalAdsword: number;
  totalLink: number;
  totalSerachResutls: number;
  htmlSource: string;
}

export default function Home() {
  const router = useRouter();
  const query = router.query;
  const [isOpen, setOpen] = useState<boolean>(false);
  const [htmlSource, setHtmlSource] = useState<string | undefined>(undefined);
  const authState = useSelector(selectAuthState);
  const handleLinkClick = (data: unknown) => {
    const keyword = data as KeywordDto;
    setOpen(true);
    setHtmlSource(keyword.htmlSource);
  };

  const headers: TableHeader[] = [
    {
      name: "id",
      title: "#Id",
      align: "left",
      type: "text",
    },
    {
      name: "name",
      title: "#Keyword",
      align: "left",
      type: "block",
    },
    {
      name: "adsWordCount",
      title: "Ads Word Count",
      align: "left",
      type: "text",
    },
    {
      name: "linkCount",
      title: "Link Count",
      align: "left",
      type: "text",
    },
    {
      name: "searchResultCount",
      title: "Search Results",
      align: "left",
      type: "text",
    },
    {
      name: "View",
      title: "Action",
      align: "left",
      type: "action",
      action: handleLinkClick,
    },
  ];
  useEffect(() => {
    if (!authState.isAuthenticated) {
      router.push("/sign-in");
    }
  }, [authState, router]);
  const {
    data: keywords,
    error,
    isValidating,
  } = useGetKeyWords({
    keyword: query.keyword as string,
    page: query.page as unknown as number,
    limit: 10,
  });
  const handlePaginationChange = (value: number) => {
    const qParams: { page?: string } = {};
    if (value) {
      qParams.page = value.toString();
    } else {
      delete query.page;
    }
    const urlSearchParams = { ...query, ...qParams } as Record<string, string>;
    void router.replace({
      pathname: location.pathname,
      search: new URLSearchParams(urlSearchParams).toString(),
    });
  };
  return (
    <>
      <Head>
        <title>Google Scrapper</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Header />
      <main className={styles.main}>
        <Grid
          container
          justifyContent={"space-between"}
          alignItems="center"
          my={2}
        >
          <Grid item xs={8} md={8}>
            <Box
              sx={{
                display: "flex",
                alignContent: "center",
                alignItems: "center",
                py: 3,
                px: 2,
              }}
            >
              <SearchFilterForm
                initialValues={{ keyword: "" }}
                placeholder="Type your search Keyword here ..."
              />
            </Box>
          </Grid>
          <Grid item xs={2} md={2}>
            <Button
              variant="contained"
              type="button"
              fullWidth
              // disabled={isLoading}
            >
              Upload
            </Button>
          </Grid>
        </Grid>

        <CustomTable
          headers={headers}
          isLoading={isValidating}
          data={(keywords?.items as unknown as Record<string, unknown>[]) || []}
        />

        <PaginationComponent
          count={keywords ? keywords?.meta.totalPages : 0}
          current={keywords ? keywords.meta.currentPage : 1}
          size="medium"
          onChange={handlePaginationChange}
        />
        <DialogComponent
          isOpen={isOpen}
          title="HTML SOurce"
          onClose={() => {
            setOpen(false);
          }}
        >
          <Card>
            <div dangerouslySetInnerHTML={{ __html: htmlSource || "" }}></div>
          </Card>
        </DialogComponent>
      </main>
    </>
  );
}
